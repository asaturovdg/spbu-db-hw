CREATE TEMP TABLE high_sales_products AS
SELECT * FROM sales 
WHERE quantity > 10 AND sale_date BETWEEN CURRENT_DATE - '7 days'::INTERVAL AND CURRENT_DATE;

SELECT * 
FROM high_sales_products
LIMIT 5;

WITH employee_sales_stats AS (
	SELECT employee_id, SUM(quantity) AS sum_quantity, AVG(quantity) AS avg_quantity
	FROM sales
	WHERE sale_date BETWEEN CURRENT_DATE - '30 days'::INTERVAL AND CURRENT_DATE
	GROUP BY employee_id
)
SELECT ess.employee_id, e.name, ess.sum_quantity, ess.avg_quantity
FROM employee_sales_stats ess
JOIN employees e ON ess.employee_id = e.employee_id
LIMIT 5;

WITH employees_hierarchy AS (
	SELECT e.manager_id AS manager_id, e2.name AS manager_name, e.employee_id AS employee_id, e.name AS employee_name
	FROM employees e 
	LEFT JOIN employees e2 ON e.manager_id = e2.employee_id 
)
SELECT *
FROM employees_hierarchy
WHERE manager_name = 'Alice Johnson'
LIMIT 5;

WITH monthly_sales AS (
	SELECT product_id, date_part('MONTH', sale_date) AS sale_month, sum(quantity) AS sum_quantity
	FROM sales
	GROUP BY product_id, sale_month
)
(SELECT *
FROM monthly_sales
WHERE sale_month = date_part('month', CURRENT_DATE)
ORDER BY sum_quantity DESC 
LIMIT 3)
UNION ALL
(SELECT *
FROM monthly_sales
WHERE sale_month = date_part('month', CURRENT_DATE - '1 months'::interval)
ORDER BY sum_quantity DESC 
LIMIT 3);

EXPLAIN ANALYZE 
SELECT product_id, sum(quantity)
FROM sales
GROUP BY product_id 
LIMIT 5;

